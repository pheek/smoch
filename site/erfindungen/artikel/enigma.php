	<h1>Geschichte</h1>
	<p>Bereits vor 1918 wurden verschiedene Rotor-Chiffriermaschinen entwickelt. Die im 2. Weltkrieg eingesetzte Enigma wurde 1918 entwickelt.</p>
	 
	<h1>Verwendung und Nutzen</h1>
                         <p>Mit einer Chiffriermaschine kann ein
													 Klartext in einen Krypto-Text umgewandelt
													 und wieder zurückverwandelt werden. Wer den
													 Schlüssel nicht kennt, kann den (geheimen)
													 Klartext nur erahnen.</p>
  <p>Die abgebildete «Enigma» wurde 2018 vom Kurator im Deutschen
  Museum abgelichtet.</p>
	
	<h1>Funktionsweise und Aufbau</h1>
	<p>In der Enigma wurden die Buchstaben mit elektrischen Leitern an
	den Walzen weitergeleitet und am Schluss an einer "Umkehrwalze"
	zurückgeleitet. Somit wurde jedem Buchstaben ein anderer Buchstabe
	zugewiesen. Da die Einstellung der Walzen geheim war (das war der
	eigentliche Schlüssel), gab es unzählige Varianten, die Walzen
	einzustellen und ein "Knacken" des Codes schien lange Zeit unmöglich.</p>

	<h1>Trivia</h1>
	<p>Dass jeder Buchstabe niemals mit sich selbst verschlüsselt wurde,
	aber auch die Tatsache, dass gewisse Texte (Wetterberichte,
	Schlussfloskeln) immer wieder angewendet wurden, machten Alan Turing
	und seinem Team das Entschlüsseln der Enigma schlussendlich
		möglich. Turing baute die "Enigma-Bombe", eine
	Elektsich/Mechanische Maschine, welche als ein Baustein in der
	Geschichte der heutigen Computer angesehen werden kann. Die Theorie
	der "Turing Maschine" (was mit der Enigma-Bombe nur entfernt zu tun
	hat), auch von Alan Turing, gilt hingegen als <b>das</b>
	theoretische Konzept, das sich heutige Von-Neumann-Maschinen zu
	nutze machen und somit den modernen Computer erst ermöglichte.</p>
	<p>Mit heutigen asymmetrischen Schlüsseln basierend auf großen
	Primzahlen und/oder diskreten Logarithmen ist ein Entschlüsseln
	beinahe unmöglich. Mathematisch absolut sicher ist hingegen nur der
	One-Time-Pad, welcher aber einen geheimen Schlüsselabtausch vor der
	Verschlüsselung benötigt.</p>

