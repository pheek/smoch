	<h1>Geschichte</h1>
	<p>Die ersten Taschenrechner wurden von Texas-Instruments 1967
	entwickelt:</p>
  <p class='zitat text'>In 1965, Texas Instruments, who had recently developed the first integrated circuits, needed an application to demonstrate their advantages in miniaturising complex electronics, and so started development of the "CalTech" prototype pocket calculator. The completed prototype was demonstrated two years later, and it spurred a number of companies to produce commercial devices, mostly using chips manufactured by TI, long before TI themselves became a manufacturer of calculators for end-users. The early devices printed their results onto paper tape.</p>
	<p class='zitat quelle'><a href='https://www.calculator.org/articles/A_First_Calculator.html'>www.calculator.org/articles/A_First_Calculator.html</a></p>
	 
	<h1>Verwendung und Nutzen</h1>
	<p>Um einfache Berechnungen durchzuführen hat der Taschenrechner
	rasch den <a href='https://smoch.ch/erfindung.php/zahlenschieber'>Zahlenschieber</a> oder den <a href='https://smoch.ch/erfindung.php/abakus'>Abakus</a> abgelöst.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Ein <a href='https://smoch.ch/erfindung.php/chip'>Rechnerchip</a> sorgt für Addition, Subtraktion, Multiplikation
	und Division.</p>

	<h1>Trivia</h1>
       <p><tt>LESE ESEL</tt>, waren die ersten Spielchen, die wir mit
       dem Taschenrechner in der Sekundaschule ca. 1980 anstelle von
       Kurznachrichten verwendet hatten. Da nur die Ziffern 0-9 und
       der Dezimapunkt angezeigt werden konnten, musste man sich schon
       was überlegen, um Texte wie "<tt>LESE'ESEL</tt>" auf dem
				 Rechner erscheinen zu lassen.</p>
