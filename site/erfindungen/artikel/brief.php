	<h1>Geschichte</h1>
	<p>Schon die Babylonier (1800 v. Chr.) nutzten bereits Tontafeln, um Nachrichten zu übermitteln.
	   Die Ägypter verwendeten dann <a href="https://de.wikipedia.org/wiki/Papyrus">Papyrus</a>.</p>
	 
	<h1>Verwendung und Nutzen</h1>
  <p>Vor Telefax, Telephon und Internet waren Briefe die gängige Art, Nachrichten aller Art weiterzuleiten.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Auf der Vorderseite wird die Zieladresse angegeben und in der Regel eine <a href="https://de.wikipedia.org/wiki/Briefmarke">Briefmarke</a>, sodass das Porto nicht vom Empfänger bezahlt werden muss.</p>

