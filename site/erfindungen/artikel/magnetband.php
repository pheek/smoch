	<h1>Geschichte</h1>
	<p>Die ersten magnetischen Aufzeichnungen auf Draht wurden 1898
	erstellt.</p>

	<h1>Verwendung und Nutzen</h1>
	<p>Neben analogen Daten, wie Musik- und Sprachaufzeichnung,
	   werden in der Informationstechnik vorwiegend Daten wie Programme
	   oder Records von Stamm-, Bewegungs- oder Bestandesdaten.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Auf einem Trägermaterial ist eine magnetisierbare Schicht
	   aufgebracht oder darin eingelassen. Diese Schicht kann mit einem
	   Schreibkopf beschrieben bzw. mit einem Lesekopf wieder ausgelesen
	   werden. Dabei wird zwischen magnetischem Nord- bzw. Südpol unterschieden.
	   Eine spezielle Möglichkeit für das Speichern von Daten ist das Verwenden
	   unterschiedlicher Frequenzen für Null- bwz. Eins-Bit (wie im Sharp
	   PC-1500 verwendet).</p>

	<h1>Trivia</h1>
	 <p> Das im SMOCH ausgestellte Magnetband ist ein 0.5 Zoll
	Magnetband aus einem Nachlass.</p>

