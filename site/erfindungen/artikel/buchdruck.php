 	<h1>Geschichte</h1>
       <p>Der <a href='https://de.wikipedia.org/wiki/Buchdruck#Europa_und_Gutenberg'>Buchdruck</a> wurde schon lange vor <a href='https://de.wikipedia.org/wiki/Johannes_Gutenberg'>Johannes Gutenberg</a> in <a href='https://de.wikipedia.org/wiki/Blockdruck'>China erfunden</a> (ca. 8. / 9. Jh.). Da im Chinesischen jedoch unglaublich viele Schriftzeichen vorhanden sind, wurde erst die «Neuerfindung» durch Gutenberg im 15. Jahrhundert effizient benutzbar.</p>
	 
	<h1>Verwendung und Nutzen</h1>
                         <p>Da bis vor der Erfindung der Druckpresse die Bücher abgeschrieben wurden, verlieh der Buchdruck der Verbreitung von Texten aller Art einen starken Aufschwung:</p>
    <p><span class='zitat text'>Der Buchdruck ermöglichte die exakte Reproduktion von Wissen in einem zuvor nie gekannten Ausmaß</span> <span class='zitat quelle'><a href='https://de.wikipedia.org/wiki/Buchdruck#Europa_und_Gutenberg'>Wikipedia</a> (2020)</span>.</p>

	<h1>Funktionsweise und Aufbau</h1>
                             <p>Bewegliche <a href='https://de.wikipedia.org/wiki/Letter'>Lettern</a> werden auf Druckformen (mittels <a href='https://de.wikipedia.org/wiki/Winkelhaken'>Winkelhaken</a>) gesetzt und diese — sobald mit Druckertinte benetzt — auf das Papier gepresst.</p>

	<h1>Trivia</h1>
       <p> Für die PDF-Versionen von Büchern wird heutzutage weder Letter noch Tinte verwendet; so wurde Das Buch «<a href='https://programmieren-lernen.ch/buch'>Programmieren lernen</a>» ISBN 978-3-280-04066-9 nicht mehr mit Lettern sondern in der Sprache in <a href='https://www.latex-project.org'>LaTeX</a> «gesetzt».</p>

