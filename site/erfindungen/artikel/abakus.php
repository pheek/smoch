	<h1>Verwendung und Nutzen</h1>
	<p>Jede Reihe, worauf die Steinchen oder Holzkugeln aufgefädelt sind, haben einen anderen Stellenwert. Somit kann im Zehnersystem, aber auch im 60er System (Babylon) gerechnet werden. Dabei werden die Kügelchen mit den Fingern verschoben, um die Zahlen zu repräsentieren.</p>
	 

	<h1>Funktionsweise und Aufbau</h1>
	<p>Auf Schnüren, Holzstäbchen, Drähten o. ä. werden Kugeln oder Plättchen aufgefädelt. Diese können nach unten und oben (bzw. links und rechts) verschoben werden. Die Anzahl der verschobenen Kugeln werden zusammengezählt und repräsentieren je nach Stellenwert eine ander Zahl.</p>

	<h1>Fun-Facts</h1>
