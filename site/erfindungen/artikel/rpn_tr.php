	<h1>Geschichte</h1>
	<p>Der erste Wissenschaftliche Taschenrechner ist der HP 35 der Firma Hewlett und Packard (HP inc.) im Jahre 1972</p>

 <h1>Verwendung und Nutzen</h1>
 <p>Als erster Taschenrechner mit Wissenschaftlichen Funktionen wie Logarithmus und trigonometrische Funktionen musste sich HP etwas über die Bedienug, insbesondere der Vorrangregeln, Gedanken machen. Um Missverständnisse bei den Vorrangregeln von vornherein auszuschließen hat sich HP für die umgekehrte polnische Notation (<a href="https://de.wikipedia.org/wiki/Umgekehrte_polnische_Notation">upn</a>, engl. rpn für «reverse polish notation») entschieden. Dabei werden erst die Zahlen und danach die Operatoren eingegeben:
<br />
5 + 4 * 3 wird also zu 5 4 3 * +
<br />
(5+4) * 3 hingegen wird 5 4 + 3 *
<br />
Dies hat zwei Vorteile: Erstens ist die Ausführungsreihenfolge immer klar und zweitens werden somit keine Klammern nötig.
</p>


<h1>Funktionsweise und Aufbau</h1>
<p>Der HP 35 hat ein rotes Display mit Leuchtdioden. Der Stapel beläuft sich auf vier Zahlen. Somit konnte man bis zu vier Zahlen eingeben, ehe man diese durch Operationen miteinander verrechnete.</p>

	<h1>Trivia</h1>
<p>Dank einer Online-Auktion gelang dieses Exponat des weltweit ersten wissenschaftlichen Taschenrechners ins smoch.ch</p>
