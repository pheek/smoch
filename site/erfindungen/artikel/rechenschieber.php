	<h1>Geschichte</h1>
	 <p>Rechenschieber gehen nach diversen (nicht genannten, aber sich deckenden) Quellenangaben bis ins 17. Jahrhundert zurück. Angegeben wird z. B. Gunter's<!-- ' --> Scale aus dem Jahre 1620 (s. Quellenangabe unten).</a></p>
	 
	<h1>Verwendung und Nutzen</h1>
	 <p>Mit dem Rechenschieber können einfach zwei Zahlen multipliziert werden. Ebenso sind Winkelfunktionen und Logarithmen ablesbar.</p>

	<h1>Funktionsweise und Aufbau</h1>
	 <p>Indem die Logarithmen addiert werden, multiplizieren sich die zugehörigen Basiszahlen. Dazu sind die Zahlen in sich logarithmisch verkleinernden Abständen angeordnet; durch die Verschiebung der "Lineale" werden die Logarithmen addiert.</p>

