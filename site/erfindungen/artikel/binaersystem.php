	<h1>Geschichte</h1>
       <p>Das Binärsystem als Zahlensystem wurde 1692 von <a href='https://de.wikipedia.org/wiki/Gottfried_Wilhelm_Leibniz'>Gottfried Wilhelm Leibniz</a> erschaffen; lange, bevor es seinen
  Platz in der Geschichte der elektronischen Rechenanlagen fand.</p>

	<h1>Verwendung und Nutzen</h1>
                         <p>Analogrechner sind definitiv schneller als ihre digitalen Kollegen. Leider ist das modellieren eines Problems mit dem Analogrechner ungleich schwieriger und oft mit bauen einer dem Problem angepassten Hardware verbunden. Die digitale Welt bedient sich daher lediglich zweier Zustände, dem sog. "Bit";
   <ul>
    <li>Strom / kein Strom</li>
    <li>Positiv magnetisiert / negativ magnetisiert</li>
    <li>Loch in der Lochkarte / kein Loch an der Stelle</li>
<li>Spannung auf einer Leitung, keine Spannung auf der Leitung</li>
    <li>hoher Ton / tiefer Ton (Beim Modem: moduliert /
			de moduliert)</li>
		<li>Kondensator geladen / Kondensator entladen</li>
		<li>Pixel (Picture Element) leuchtet / Pixel ist aus</li>
		<li>...</li>
		<li>...</li>
   </ul>
	 <p>
  Da im Computer aber auch Zahlen modelliert werden sollten, bietet
  sich das Binärsystem geradezu an. Jede Ziffer benötigt genau einen
  von zwei Zuständen.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Im Gegensatz zum Dezimalsystem (auch Zehnersystem genannt),
	benötigt das Binärsystem nicht zehn, sondern lediglich zwei
		Ziffern. Wie auch das Zehnersystem ist das Binärsystem ein
	Stellenwertsystem, jedoch nicht mit den Stellenwerten 1, 10, 100,
	1000, ... sondern mit den Stellenwerten 1, 2, 4, 8, 16, 32, 64, ....</p>
	

	<h1>Trivia</h1>
       <p>Obschon das Binärsystem nur aus zwei Zuständen (für jede Ziffer) besteht, gibt es dennoch viele weitere Wörter und Vorsilben für die Zahl Zwei: bi-, di-, zwie-, doppel, binär, duo, dual, Boole'sch, paar, stereo, sekund-, ...</p>

