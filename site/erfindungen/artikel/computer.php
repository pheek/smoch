		<h1>Geschichte</h1>
	 <p>Die Geschichte des Computers reicht natürlich mit allen Vorbereitungen weiter zurück als der Bau des Zuse Z3 von Konrad Zuse im Jahre 1937. Für den Z3 war vor allem die Erfindung des Relais von Bedeutung.</p>
	 
	 <p>Der Z3 war noch nicht voll Turing-Programmierbar, sondern konnte lediglich eine Schleife (unbedingter Sprung zurück) vollziehen.</p>
		 
		 Weitere Vorbereitungen zu heutigen Computern trafen Allan Turing mit seiner Turingmaschine (1936) und später die "Von Neumann-Architektur" von John von Neumann (1947).</p>

	 <h1>Zu Z1 bis Z3</h1>
	 <p class='zitat text'>
	 
<b>Z3: Erster funktionsfähiger
programmgesteuerter Rechenautomat</b><br />

Zuses «Bedingungskombinatorik» und die «abstrakte Schaltgliedtechnik»
erlaubten den unmittelbaren Nachbau des mechanischen Gerätes Z1
als Relais-Rechenmaschine.
Zuse folgte dabei dem Rat seines Freundes Schreyer.
Er baute ab 1938 ein Rechenwerk, später Z3 genannt, in Relaistechnik.
Zuse machte mit diesem Versuchsgerät wertvolle Erfahrungen.
<br />
Mitte 1940 begann Zuse mit dem Bau der Z3.
Sie war im Mai 1941 funktionsfähig.
Das Rechenwerk war elektromechanisch mit 600 Relais,
der Speicher aus 1400 Relais aufgebaut.
<br />
Die Z3 kostete rund 25 000 RM.
Sie wurde 1943 bei einem Luftangriff beschädigt und 1944 zerstört.
<br />
Die Z3 von 1941 ist der erste voll funktionsfähige,
frei programmierbare, programmgesteuerte binäre Rechenautomat.
Der Programmablauf erfolgte in <i>einer</i> Schleife.

</p>
