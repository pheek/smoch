	<h1>Verwendung und Nutzen</h1>
	<p>Computer werden für verschiedenste Berechnungen (rechnen engl. 'to compute'), Arbeiten und die elektronische Post (E-Mail); aber auch zum <i>Surfen</i> im <i>Web</i>, zum Spielen oder einfach (wie Linus Torwalds - der Erfinder von Linux und Git - es nennt) <b>just for fun</b> verwendet; wie der Autor dieser Seiten seine Computer vorwiegend zum Programmieren benutzt (und eher selten als Werkzeug).

