	<h1>Geschichte</h1>
       <p>Der <i>Educated Monkey</i> taucht 1916 auf und als sein Erfinder wird William H. Robertson genannt: <a href='https://americanhistory.si.edu/blog/consul-educated-monkey-or-inventions-william-h-robertson'>si.edu</a></p>
	 
	<h1>Verwendung und Nutzen</h1>
	<p>Mit dem <i>Monkey</i> kann das kleine Einmaleins gerechnet werden. Dabei werden die beiden Zeiger (an den Füßen des Monkey) auf zwei Zahlen gestellt und zwischen den Händen des Monkey wird die Multiplikation angezeigt.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Der Monkey funktioniert ähnlich wie ein <a href='https://de.wikipedia.org/wiki/Pantograph'>Pantograph</a>. Da die beiden Zeiger nicht auf die selbe Zahl gestellt werden können, steht eine Extra-Tabelle für die Quadratzahlen zur Verfügung.</p>

	<h1>Fun-Facts</h1>
	<p>Das Exponat im SMOCH stammt aus dem Museumsladen des <i>Haus' Milà</i> von Gaudí in Barcelona. Die beiden haben nichts gemeinsam, außer, dass sie zur selben Zeit am selben Ort waren (Jugendstil).</p>

