	<h1>Geschichte</h1>
	<p>Die Neperstäbchen oder auch Napierstäbchen wurden von John Napier im 17. Jahrhundert entwickelt.</p>
  <p class='zitat text'>Nachbildung nach Photographien des Originals. Ursprünglich im Kloster Andechs, seit 1912 im Deutschen Museum ; 1944 zerstört.</p>
	<p class='zitat quelle'>Text im Deutschen Museum 2018</p>
	 
	<h1>Verwendung und Nutzen</h1>
	<p>Diese Stäbchen helfen Zahlen zu multiplizieren und kommen dem schriftlichen Multiplizieren der Araber nahe.</p>
  <p>Auf <a href='https://www.lernhelfer.de/schuelerlexikon/mathematik/artikel/napierstaebchen'>Lernhelfer.de</a> wird gezeigt, wie man mit Neperstächen rechnen kann.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Es handelt sich um einfache Holzstäbchen, welche das kleine "Einmaleins" enthalten und welche zum Multiplizieren nebeneinander gelegt werden.</p>

	<h1>Fun-Facts</h1>
	<p>John Napier gilt auch als einer der Erfinder der Logarithmentafeln.</p>

