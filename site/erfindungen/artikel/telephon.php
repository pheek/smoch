	<h1>Verwendung und Nutzen</h1>
                         <p>Wer auf die Non-Verbale Kommunikation gerne verzichtet und gerne viel plaudert, ist mit einem Telefon gut bedient.</p>

	<h1>Funktionsweise und Aufbau</h1>
                             <p>Ursprünglich wurden zwei Endgeräte mittels Kabel direkt miteinander verbunden. Dazu musste man sich zunächst bei der Zentrale melden und angeben, mit wem man Telephonieren wollte. Später wurden Automaten eingeführt, die eine physische Verbindung nach eingegebenenr Nummer (ein Puls-Verfahren)
selbständig herstellen konnten. Neuerdings werden die Verbindungen meist nur noch vituell hergestellt und im Hintergund läuft ein Paket-Dienst (Voice over IP).</p>

	<h1>Trivia</h1>
	<p>Nach den Kurbeltelephonen kamen die Wählscheibentelephone. Da wir aber immer noch wussten, dass das Telephon auf einem Unterbrechungs-Mechanismus beruhte, konnte man anstelle des Drehens an der Wählscheibe die Gabel unterbrechen. Anstelle einer Vier z. B. konnte man einfach vier mal kurz hintereinander auf die Gabel schlagen. Im Studentenhaus wurde dann ein Telephon angebracht, das keine Wählscheibe hatte. Die PTT (vorgänger der Swisscom) ging naiv davon aus, dass ohne Wählscheibe nicht telephoniert werden könne. Was meint ihr, wie oft haben unsere türkischen Komilitionen damit gratis in die Türkei telephoniert? 
</p>

