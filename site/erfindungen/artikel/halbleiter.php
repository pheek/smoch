	<h1>Halbleiter: Im Aufbau</h1>

	<h1>Geschichte</h1>
  <p>Das Element Silizium war im Universum schon viel länger vorhanden, als dass es Leben auf unserem Planeten gab.
<br/>
  Spannend ist aber wohl eher die Geschichte der Diode (anfänglich mit Bleisulfidkristallen 1874 - F. Braun) und das
  Dotieren der Halbleiter: Wie z. B. das Einbringen von Aluminium oder Phosphor in ein Silizium Kristallgitter, um aus dem Halbleiter einen Stromleiter mit speziellen Oberflächeneigenschaften zu erhalten.</p>
	 
	<h1>Verwendung und Nutzen</h1>
		<p>Halbleiter werden neben anderem zur Herstellung von
			<a href='https://de.wikipedia.org/wiki/Diode'>Dioden</a> oder <a href='https://de.wikipedia.org/wiki/Transistor'>Transistoren</a> verwendet.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Die klassische Erklärung basiert auf zwei Silizium-Schichten, wo
	eine davon positiv dotiert ist und die andere negativ. Das Dotieren
	geschieht mit einbringen von wenigen Fremdatomen (z. B. Phosphor in
	Silizium). Die negativ dotierte Schicht enthält für das
	Silizium-Kristallgitter zu viele Elektronen (also zu viele negative
	Ladungsträger) und die positiv dotierte Schicht enthält zu wenige
	Elektronen. Die fehlenden Elektronen werden auch als "positive
	Löcher" angesehen. Spannend ist nun die Übergangsschicht zwischen
	den beiden dotierten Schichten. Da die «überzähligen» Elektronen
	fast automatisch zu den positiven «Löchern» fließen wollen, fließt
	der (negative Elektronen-)Strom in Richtung positive Schicht fast
	verlustfrei. In die andere Richtung hingegen zieht der positive
				Pol die Elektronen aus der Mitte der Diode ab (und umgekehrt
	wandern die «Löcher» zum Minuspol). Somit entsteht zwischen den
			beiden Schichten eine Sperrschicht, wo keine Elektronen
				hindurchfließen können. Doch ein Video sagt mehr als tausend Worte:

				Youtube: <a href='https://www.youtube.com/watch?v=660G24GbdvM'>Diode Aufbau und Funktionsweise</a>.
</p>

	<h1>Fun-Facts</h1>
