	<h1>Geschichte</h1>
	 <p class='zitat text'>Ab 1642 entwickelte Blaise Pascal seine "Pascaline", eine mechanische Rechenmaschine, für die Addition und Subtraktion sechsstelliger Zahlen. <span class='zitat quelle'>Quelle: <a href="https://www.mathematik.de/spudema/spudema_beitraege/beitraege/sujan/pascaline.htm">https://www.mathematik.de/spudema/spudema_beitraege/beitraege/sujan/pascaline.htm</a></span></p>
	 
	<h1>Verwendung und Nutzen</h1>
	 <p>Pascal entwickelte die Pascaline nicht zuletzt, um seinem Vater bei täglichen Rechenaufgaben zu helfen.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p>Die Pascaline ist rein mechanisch gebaut.</p>

