  <h1>Geschichte</h1>
	Mitte 20er Jahre des 20. Jahrhunderts wurden die mechanischen Rechenmaschinn durch sog. "Vollautomaten" abgelöst. Das drehen der Kurbel wurde durch Elektomotoren ersetzt. Nach dem Eingeben der Zahlen rechnet die Maschine selbständig: Siehe <a href='https://www.technikum29.de/de/rechnertechnik/elektro-mechanik.php'>technikum29.de</a>


  <h1>Verwendung und Nutzen</h1>
	<p>Die Maschinen konnten wie die rein mechanischen Maschinen Addition, Subtraktion, Multiplikation und Division ausführen. Da die Division einige Schritte in Anspruch nimmt, war diese von Hand jeweils mühsam. Die elektromechanischen Maschinen konnten die Operationen selbständig ausführen und Zeit und Kraft sparen.</p>

  <h1>Funktionsweise und Aufbau</h1>
	<p>Im Wesentlichen sind die selben Zahnräder und Walzen wie bei den rein mechanischen Maschinen vorhanden. Der Hauptunterschied liegt in der Verwendung eines Elektormotors, der die zeitaufwändigen und teilweise auch anstrengenden Kurbelbewegungen einem abnahm. Gerade bei der Division konnte so viel Zeit eingespart werden.</p>

  <h1>Trivia</h1>
