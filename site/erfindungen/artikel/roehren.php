	<h1>Geschichte</h1>
 <p>Röhrencomputer sind das Bindeglied zwischen Relais-Rechnern und
		Rechnern, welche auf Halbleiterbasis funktionieren.
	</p> 

	<h1>Verwendung und Nutzen</h1>
	<p>Röhrenrechner haben theoretisch keinen neuen Nutzen gegenüber den
	Relais-Rechnern.</p>

	<h1>Funktionsweise und Aufbau</h1>
	<p> Der wichtigste Vorteil von Röhren gegenüber Relais ist jedoch, dass
	Röhrenrechner keine mechanischen Teile benötigen, um eine Berechnung
	(AND, NOT, OR, XOR, ...) durchzuführen. </p>


		<h1>Fun-Facts</h1>
		<p>Die abgebildete Röhre stammt aus einem Röhrenverstärker und hat
		direkt nichts mit einem Röhrencomputer zu tun. Einzig die
		Tatsache, das die Röhre, analog zum Transistor, wie ein Verstärker
		funktioniert, der auch als Schlater verwendet werden kann.</p>


