	<h1>Verwendung und Nutzen</h1>
	 <p>Die klassische 3½-Zoll Diskette konnte ca. 1.4 Megabyte an Datenvolumen speichern. Darauf konnten Betriebbsystm-Kerne (z. B. AMIGA-Kick-Star), graphische Benutzeroberflächen (z. B. AMIGA-Workbench) aber auch Anwendungssoftware, Entwicklersoftware, Spiele oder Daten gespeichert werden.</p>
